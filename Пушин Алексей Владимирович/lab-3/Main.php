			<h2>Marvel Comics</h2>	
			<img class="img1" src="https://img3.goodfon.ru/original/2000x1230/6/b2/avengers-age-of-ultron-6122.jpg">				
			<p><a href="https://ru.wikipedia.org/wiki/Marvel_Comics">Marvel Comics(от англ. marvel «чудо»)</a>  — американская компания, издающая комиксы, подразделение корпорации Marvel Entertainment.</p> <p>Получившая среди поклонников комиксов прозвище «Дом идей», Marvel наиболее известна такими сериями комиксов, как «Фантастическая четвёрка», «Люди Икс», «Мстители», «Стражи Галактики», «Защитники», «Человек-паук», «Росомаха», «Железный человек», «Капитан Америка», «Тор», «Халк», «Дэдпул», «Сорвиголова», «Каратель», «Доктор Стрэндж», <img class="img2" src="https://s1.1zoom.ru/b5050/63/Avengers_Age_of_Ultron_479199_2048x1536.jpg">«Чёрная Пантера», «Человек-муравей и Оса», «Капитан Марвел», «Веном», «Серебряный Сёрфер», «Призрачный гонщик», «Блэйд», «Чёрная вдова», «Соколиный глаз», «Люк Кейдж», «Железный Кулак» и «Джессика Джонс». Большинство персонажей комиксов Marvel обитают в одной вымышленной вселенной, получившей название Земля-616.</p>
			
			<p>С 1960-х Marvel Comics — одна из двух самых крупных американских компаний, выпускающих комиксы, наравне с DC Comics (чьими самыми известными детищами являются «Супермен», «Бэтмен» и «Чудо-женщина», а также «Флэш» и «Зелёный Фонарь»).</p>

			<p>В конце августа 2009 года компания <a href="https://ru.wikipedia.org/wiki/The_Walt_Disney_Company">The Walt Disney Company</a> объявила о покупке Marvel[1]. Сделка состоялась в конце декабря 2009 года, и её цена составила $4,24 миллиарда[2].</p><br>
